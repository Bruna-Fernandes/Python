# 01_A imobiliária Imóbilis vende apenas terrenos retangulares. Faça um algoritmo para
#ler as dimensões de um terreno e depois exibir a área do terreno.

larg = float(input('Digite a largura do terreno (m): '))
comp = float(input('Digite o comprimento do terreno (m): '))

area = larg * comp
print('A área do terreno é: ',area)